#include <unistd.h>
#define nb "0123456789"

void	nbr(int n)
{
	if (n > 9)
		nbr (n / 10);
	write (1, &nb[n % 10], 1);
}

int	main(int ac, char **av)
{
	(void)av;
	nbr (ac - 1);
	write (1, "\n", 1);
}
